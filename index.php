    // Toggle chat visibility when the button is clicked
    function toggleCopilot() {
        const chatBox = document.getElementById("copilot-chat");
        
        // Debugging log
        console.log("Toggling copilot chat visibility...");

        // Toggle the form-hidden class on chat box to show/hide
        chatBox.classList.toggle("form-hidden");

        // Check if the chat is being displayed correctly
        if (chatBox.classList.contains("form-hidden")) {
            console.log("Chat is now hidden");
        } else {
            console.log("Chat is now visible");
        }
    }

    // Handle user input and bot response
    function handleInput(event) {
        // Check if the key pressed is 'Enter'
        if (event.key === "Enter") {
            const input = document.getElementById("copilot-input");
            const messages = document.getElementById("copilot-messages");
            const userText = input.value.trim();

            if (userText !== "") {
                // Log the user input to the console (debugging)
                console.log("User typed:", userText);

                // User message
                const userMessage = document.createElement("div");
                userMessage.className = "message user";
                userMessage.textContent = userText;
                messages.appendChild(userMessage);

                // Bot response (this is where you can replace with real response logic)
                const botMessage = document.createElement("div");
                botMessage.className = "message bot";
                botMessage.textContent = generateBotResponse(userText);
                messages.appendChild(botMessage);

                // Clear the input field after submission
                input.value = "";

                // Scroll to the bottom of the chat to show the latest messages
                messages.scrollTop = messages.scrollHeight;

                // Log the bot response for debugging
                console.log("Bot response:", botMessage.textContent);
            }
        }
    }

    // Generate a simple bot response based on user input
    function generateBotResponse(input) {
        const lowerInput = input.toLowerCase();

        // Custom responses based on keywords
        if (lowerInput.includes("hello") || lowerInput.includes("hi")) {
            return "Hello! How can I assist you today?";
        } else if (lowerInput.includes("how are you")) {
            return "I'm doing great, thank you for asking!";
        } else if (lowerInput.includes("help")) {
            return "I am here to help you. Ask me anything!";
        } else if (lowerInput.includes("features")) {
            return "You can ask me about your tasks, upcoming events, and more!";
        } else if (lowerInput.includes("what is your name")) {
            return "I am your task assistant! Feel free to ask me anything.";
        } else {
            return "Sorry, I didn't understand that. Can you please ask something else?";
        }
    }
</script>

